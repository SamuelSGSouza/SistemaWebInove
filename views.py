def dadosddd_dashboard(request):
    if request.GET.get("verificar", "") == "Verificar":
        executa_obtencao_ddds()

    # --- Filtros (GET) ---
    q = (request.GET.get("q") or "").strip()
    uf = (request.GET.get("uf") or "").strip()
    mei = (request.GET.get("mei") or "").strip()
    ddd = (request.GET.get("ddd") or "").strip()

    qs = DadosDDD.objects.all()
    if q:
        qs = qs.filter(telefone__icontains=q)
    if uf:
        qs = qs.filter(uf__iexact=uf)
    if mei:
        qs = qs.filter(mei_nao_mei__iexact=mei)
    if ddd:
        qs = qs.filter(ddd__iexact=ddd)

    # --- Métricas / agregações (sobre o conjunto filtrado) ---
    total = qs.count()
    ufs_count = qs.values("uf").distinct().count()
    ddds_count = qs.values("ddd").distinct().count()

    by_mei_qs = qs.values("mei_nao_mei").annotate(total=Count("id")).order_by("-total")
    mei_map = {row["mei_nao_mei"]: row["total"] for row in by_mei_qs}
    mei_s = mei_map.get("S", 0)
    mei_percent = round((mei_s / total) * 100, 1) if total else 0.0

    by_uf_qs = qs.values("uf").annotate(total=Count("id")).order_by("-total")
    by_ddd_qs = qs.values("ddd").annotate(total=Count("id")).order_by("-total")[:15]

    # --- Dados dos gráficos ---
    chart_mei_labels = [row["mei_nao_mei"] for row in by_mei_qs]
    chart_mei_data = [row["total"] for row in by_mei_qs]

    chart_uf_labels = [row["uf"] or "-" for row in by_uf_qs]
    chart_uf_data = [row["total"] for row in by_uf_qs]

    chart_ddd_labels = [row["ddd"] or "-" for row in by_ddd_qs]
    chart_ddd_data = [row["total"] for row in by_ddd_qs]

    # --- Tabela paginada ---
    paginator = Paginator(qs.order_by("uf", "ddd", "telefone"), 25)
    page_obj = paginator.get_page(request.GET.get("page"))

    context = {
        "total": total,
        "ufs_count": ufs_count,
        "ddds_count": ddds_count,
        "mei_percent": mei_percent,
        "page_obj": page_obj,
        "filters": {"q": q, "uf": uf, "mei": mei, "ddd": ddd},

        # Serializados p/ JS
        "chart_mei_labels": json.dumps(chart_mei_labels),
        "chart_mei_data": json.dumps(chart_mei_data),
        "chart_uf_labels": json.dumps(chart_uf_labels),
        "chart_uf_data": json.dumps(chart_uf_data),
        "chart_ddd_labels": json.dumps(chart_ddd_labels),
        "chart_ddd_data": json.dumps(chart_ddd_data),
    }
    return render(request, "dadosddd_dashboard.html", context)


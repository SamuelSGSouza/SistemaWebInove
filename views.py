def lida_arquivos(request, pasta: str, model: Union[None, BaseDFV, BaseInfoCredito, BaseEnriquecimento, BaseBlackList, BaseGigaMais, BaseQuarentenaGigaMais], sistema:str) -> JsonResponse:
    try:
        if request.method != 'POST':
            return JsonResponse({'status': 'error', 'sucessos': [], "erros":['Método inválido.',], "links": [], "relatorio": []})

        # agora pega a lista de arquivos com o mesmo name do input
        arquivos = request.FILES.getlist('arquivos')
        if not arquivos and model != BaseReceita:
            return JsonResponse({'status': 'error', 'sucessos': [], "erros":['Nenhum arquivo recebido.',], "links": [], "relatorio": []})

        PASTAS_RAIZ = {
            "oi": os.path.join(os.getcwd(), "media"),
            "geral": os.path.join(os.getcwd(), "media"),
            "giga_mais": os.path.join(os.getcwd(), "media_giga_mais"),
            "janeiro_2026": os.path.join(os.getcwd(), "media_janeiro_2026")
        }
        pasta_destino = os.path.join(PASTAS_RAIZ[sistema], pasta)
        os.makedirs(pasta_destino, exist_ok=True)
        print(f"Sistema= {sistema}")
        if model in [BaseDFV, BaseBlackList, BaseInfoCredito, None]:
            
            for path in os.listdir(pasta_destino):
                file = os.path.join(pasta_destino, path)
                if os.path.isfile(file):
                    os.remove(file)
                elif os.path.isdir(file):
                    shutil.rmtree(file)


        sucessos = []
        erros = []
        links = []
        relatorio = []

        total_arqs = 0
        for arquivo in arquivos:
            destino = os.path.join(pasta_destino, arquivo.name)
            with open(destino, 'wb+') as dest:
                for chunk in arquivo.chunks():
                    dest.write(chunk)
            total_arqs += 1
            sucesso, mensagem = verifica_arquivo(request,arquivo, destino, pasta, sistema)
            if sucesso:
                sucessos.append(mensagem)
            else:
                erros.append(mensagem)

        if erros:
            return JsonResponse({'status': 'error', 'sucessos': sucessos, "erros":erros, "links": links, "relatorio": relatorio})
        
        

        if model == BaseReceita:
            processo = Process(target=fase_1_gerador)
            processo.start()

        if model == BaseDFV:
            salva_log("Iniciou sistema de extração da OI", sistema=sistema)
            processo = threading.Thread(target=exec_fases_2_3_4_e_5, args=(sistema,))
            processo.start()

        if model == BaseGigaMais:
            processo = Process(target=fases_2_4_5, args=(sistema,))
            processo.start()

        if model == BaseInfoCredito:
            salva_log("Iniciou análise de crédito", "oi")
            relatorio, erros_internos = gera_e_atualiza_dados_credito()
            relatorio = relatorio.split("\n")

            if erros_internos:
                for er in erros_internos:
                    erros.append(er)

        if pasta == "arquivos_quarentena":
            print("Entrou quarentena")
            relatorio, erros_internos = gera_e_atualiza_quarentena(PASTAS_RAIZ[sistema], model)
            relatorio = relatorio.split("\n")

            if erros_internos:
                for er in erros_internos:
                    erros.append(er)
    
        if model == BaseEnriquecimento:
            relatorio, erros_internos = gera_e_atualiza_enriquecimento()
            relatorio = relatorio.split("\n")

            if erros_internos:
                for er in erros_internos:
                    erros.append(er)

        if model == None:
            if pasta == "arquivos_filtragem":
                relatorio, erros_internos = filtra_arquivos(PASTAS_RAIZ[sistema])
            elif pasta == "arquivos_complementar":
                relatorio, erros_internos = complementa_arquivos(PASTAS_RAIZ[sistema])
            elif pasta == "arquivos_cnpj":
                relatorio, erros_internos = complementa_cnpj(PASTAS_RAIZ[sistema])

            relatorio = relatorio.split("\n")
            url_path = reverse('baixar_pasta_zip')  # ou reverse('minha_rota', kwargs={'pasta': pasta}) se tiver parâmetros nomeados
            url_relativa = f"{url_path}?pasta={pasta}&view_or_download=download"
            url_completa = request.build_absolute_uri(url_relativa)
            links.append(url_completa)

            if erros_internos:
                for er in erros_internos:
                    erros.append(er) 


        if erros:
            return JsonResponse({'status': 'error', 'sucessos': sucessos, "erros":erros, "links": links, "relatorio": relatorio})
        

        return JsonResponse({'status': 'success', 'sucessos': sucessos, "erros":erros, "links": links, "relatorio": relatorio})
    except Exception as e:
        return JsonResponse({'status': 'error', 'sucessos': [], "erros":[f"Erro desconhecido na requisição: {e}", ], "links": [], "relatorio": []})


